export async function upsertSettings(req: Request, res: Response): Promise<void> {
  const { contentType } = req.params as { contentType: string };

  const VALID_TYPES = ["oracao", "reflexao", "mensagem"];
  if (!VALID_TYPES.includes(contentType)) {
    throw new HttpError(`Tipo inválido. Válidos: ${VALID_TYPES.join(", ")}`, 400);
  }

  const body = req.body as Partial<{
    enabled: boolean;
    volume_base: number;
    ducking_db: number;
    fade_in_ms: number;
    fade_out_ms: number;
    default_category: string;
  }>;

  const [settings, created_] = await BackgroundTrackSettings.upsert({
    content_type: contentType,
    ...(body.enabled !== undefined && { enabled: body.enabled }),
    ...(body.volume_base !== undefined && { volume_base: body.volume_base }),
    ...(body.ducking_db !== undefined && { ducking_db: body.ducking_db }),
    ...(body.fade_in_ms !== undefined && { fade_in_ms: body.fade_in_ms }),
    ...(body.fade_out_ms !== undefined && { fade_out_ms: body.fade_out_ms }),
    ...(body.default_category !== undefined && { default_category: body.default_category }),
  });

  // Settings changed → invalidate all cached mixes of this type
  await backgroundTrackMixService.invalidateCacheForType(contentType);

  logger.info("admin: background track settings updated", { contentType });
  ok(res, settings, created_ ? "Configuração criada" : "Configuração atualizada");
}

/* ─── On-demand remix ────────────────────────────────────────────────────── */

export async function remixContent(req: Request, res: Response): Promise<void> {
  const contentId = Number(req.params["contentId"]);
  if (!Number.isFinite(contentId) || contentId <= 0) throw new HttpError("contentId inválido", 400);

  const content = await Content.findByPk(contentId);
  if (!content) throw new HttpError("Conteúdo não encontrado", 404);
  if (!content.audio_url) throw new HttpError("Conteúdo não tem audio_url — faça a síntese TTS primeiro", 422);

  const contentData = content as unknown as {
    id: number; tipo: string; audio_url: string;
    mixed_audio_url: string | null; background_track_id: string | null;
  };

  // Force re-mix by clearing cached URL first
  await Content.update({ mixed_audio_url: null }, { where: { id: contentId } });
  contentData.mixed_audio_url = null;

  const t0 = Date.now();
  const mixedUrl = await backgroundTrackMixService.mix(contentData);

  if (!mixedUrl) {
    throw new HttpError(
      "Nenhuma trilha disponível para este tipo de conteúdo ou mixagem desabilitada", 422,
    );
  }

  logger.info("admin: content remixed on demand", { contentId, mixedUrl, ms: Date.now() - t0 });
  ok(res, { contentId, mixedUrl, ms: Date.now() - t0 }, "Mix concluído");
}
